def terbilang(n):
  satuan = ["","satu","dua","tiga","empat","lima","enam","tujuh","delapan","sembilan"]

  if n == 0:
    return "nol"
  elif n < 10:
    return satuan[n]
  elif n == 10:
    return "sepuluh "
  elif n == 11:
    return "sebelas"
  elif n < 20:
    return satuan[n % 10] + " belas"
  elif n < 100:
    return satuan[n//10] + " puluh" + (" " + terbilang(n%10) if n%10 else "")
  elif n < 200:
    return "seratus" + (" " + terbilang(n%100) if n%100 != 0 else "")
  elif n < 1000:
    return satuan[n//100] + " ratus" + (" " + terbilang(n%100) if n%100 != 0 else "")
  elif n < 2000:
    return "seribu" + (" " + terbilang(n%1000) if n%1000 != 0 else "")
  elif n <= 9999:
    return terbilang(n//1000) + " ribu" + (" " + terbilang(n%1000) if n%1000 != 0 else "")
  else:
    return "Angka terlalu besar"

n = int(input("Masukkan Angka : "))
print(terbilang(n))


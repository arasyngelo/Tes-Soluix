def angka_romawi(n):
  if not (1 <= n <= 100):
    return "Angka tidak ada"

  angka_romawi = [
      (100, "C"), (90, "XC"), (50, "L"), (40, "XL"),
      (10, "X"), (9, "IX"), (5, "V"), (4, "IV"), (1, "I")
      ]

  hasil = ""
  for nilai, simbol in angka_romawi:
    while n >= nilai:
      hasil += simbol
      n -= nilai
  return hasil

n = int(input("Masukkan angka (1-100): "))
print(angka_romawi(n))
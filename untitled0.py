def jumlah_bilangan_prima(m):
  def prima(x) :
    if x < 2: return False
    for i in range(2, int(x**0.5) + 1):
        if x % i == 0:
          return False
    return True
  return sum(i for i in range(1, m + 1) if prima(i))

n = int(input("Masukkan Angka : "))
print(jumlah_bilangan_prima(n))

